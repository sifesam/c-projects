// STD.h
#ifndef STD_H
#define STD_H

// Define unsigned char as uint8
typedef unsigned char uint8;

// Define char as int8
typedef char int8;

// Define unsigned short as uint16
typedef unsigned short uint16;

// Define short as int16
typedef short int16;

// Define unsigned int as uint32
typedef unsigned int uint32;

// Define int as int32
typedef int int32;

// Define an enumeration type for boolean values
typedef enum {false, true} bool;

#endif // STD_H
